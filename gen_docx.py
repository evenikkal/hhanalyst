def _shade(cell, hexcolor):
    tc = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:fill"), hexcolor)
    tc.append(shd)


def make_table(doc, rows, col_widths_cm, header=True):
    t = doc.add_table(rows=len(rows), cols=len(rows[0]))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = "Table Grid"
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            cell = t.cell(ri, ci)
            cell.width = Cm(col_widths_cm[ci])
            cell.paragraphs[0].text = ""
            p = cell.paragraphs[0]
            p.paragraph_format.line_spacing = 1.0
            p.paragraph_format.space_after = Pt(2)
            p.paragraph_format.space_before = Pt(2)
            r = p.add_run(str(val))
            set_run(r, size=11, bold=(header and ri == 0))
            if header and ri == 0:
                _shade(cell, "D9E2F3")
                p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    # enforce widths
    for ci, w in enumerate(col_widths_cm):
        for cell in t.columns[ci].cells:
            cell.width = Cm(w)
    doc.add_paragraph().paragraph_format.space_after = Pt(0)
    return t


def code_listing(doc, code_text, caption=None, number=None):
    if caption:
        c = doc.add_paragraph()
        c.alignment = WD_ALIGN_PARAGRAPH.LEFT
        c.paragraph_format.space_before = Pt(8)
        c.paragraph_format.keep_with_next = True
        r = c.add_run(f"Листинг {number} – {caption}")
        set_run(r, size=12)
    for line in code_text.rstrip("\n").split("\n"):
        p = doc.add_paragraph()
        pf = p.paragraph_format
        pf.line_spacing = 1.0
        pf.space_after = Pt(0)
        pf.space_before = Pt(0)
        pf.left_indent = Cm(0.3)
        r = p.add_run(line if line else " ")
        set_run(r, font=MONO, size=9)


