# ── low-level helpers ───────────────────────────────────────────────

def set_run(run, font=FONT, size=14, bold=False, italic=False, color=None):
    run.font.name = font
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    if color:
        run.font.color.rgb = RGBColor(*color)
    rpr = run._element.get_or_add_rPr()
    rfonts = rpr.find(qn("w:rFonts"))
    if rfonts is None:
        rfonts = OxmlElement("w:rFonts")
        rpr.append(rfonts)
    for a in ("w:ascii", "w:hAnsi", "w:cs"):
        rfonts.set(qn(a), font)


def body(doc, text="", *, bold=False, italic=False, align="just", indent=True,
         size=14, space_after=0, keep=False):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.space_after = Pt(space_after)
    pf.space_before = Pt(0)
    if indent:
        pf.first_line_indent = Cm(1.25)
    amap = {"just": WD_ALIGN_PARAGRAPH.JUSTIFY, "center": WD_ALIGN_PARAGRAPH.CENTER,
            "left": WD_ALIGN_PARAGRAPH.LEFT, "right": WD_ALIGN_PARAGRAPH.RIGHT}
    pf.alignment = amap[align]
    if keep:
        pf.keep_with_next = True
    if text:
        r = p.add_run(text)
        set_run(r, size=size, bold=bold, italic=italic)
    return p


def heading(doc, text, level=1, pagebreak=False):
    p = doc.add_paragraph()
    if pagebreak:
        p.paragraph_format.page_break_before = True
    pf = p.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.space_before = Pt(12)
    pf.space_after = Pt(6)
    pf.keep_with_next = True
    pf.alignment = WD_ALIGN_PARAGRAPH.LEFT if level > 1 else WD_ALIGN_PARAGRAPH.LEFT
    r = p.add_run(text)
    set_run(r, size=16 if level == 1 else 14, bold=True)
    p.style = doc.styles[f"Heading {min(level,3)}"]
    # re-apply run formatting (style may override)
    for rr in p.runs:
        set_run(rr, size=16 if level == 1 else 14, bold=True)
    return p


def bullet(doc, text, sub=False):
    p = doc.add_paragraph(style="List Bullet")
    pf = p.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.left_indent = Cm(1.5 if not sub else 2.2)
    pf.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    r = p.add_run(text)
    set_run(r, size=14)
    return p


